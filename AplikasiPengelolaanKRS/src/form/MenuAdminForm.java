    private void tableMhsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMhsMouseClicked
        // TODO add your handling code here:

        try {
            int row = tableMhs.rowAtPoint(evt.getPoint());

            String kodeKRS = tableMhs.getValueAt(row, 0).toString();
            String npm = tableMhs.getValueAt(row, 1).toString();
            String kodeMakul = tableMhs.getValueAt(row, 2).toString();

            textKRS.setText(String.valueOf(kodeKRS));
            textNPM.setText(String.valueOf(npm));
            textMakul.setText(String.valueOf(kodeMakul));

        } catch (Exception e) {
        }
    }//GEN-LAST:event_tableMhsMouseClicked

