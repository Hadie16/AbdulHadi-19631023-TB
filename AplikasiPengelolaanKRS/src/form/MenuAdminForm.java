    private void buttonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonHapusActionPerformed
        try {
            if (
                (textKRS.getText().isEmpty()) &&(textNPM.getText().isEmpty())&&(textMakul.getText().isEmpty())){
                JOptionPane.showMessageDialog(null,"Silahkan pilih data sebelum proses hapus ");
                tableMhs.requestFocus();
            } else{

                new Konfigurasi().Hapus("ambilkrs", "kode_krs", textKRS.getText());
                new Konfigurasi().setTampilTabel(tableMhs,NamaKolom, SQL);
                new Konfigurasi().setLebarJudulKolom(tableMhs, LebarKolom);
                
                bersih();
            }
        } catch (Exception e) {
            System.out.print(e.toString());
        }
        
    }//GEN-LAST:event_buttonHapusActionPerformed

    void bersih(){
        textKRS.setText("");
        textNPM.setText("");
        textMakul.setText("");
    }
    
